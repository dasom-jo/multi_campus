N = int(input()) # 5

#     *     # i = 1 , " " : 4 (5 - 1), "*" : 1 (2 * 1 - 1)
#    ***    # i = 2 , " " : 3 (5 - 2), "*" : 3 (2 * 2 - 1)
#   *****   # i = 3 , " " : 2 (5 - 3), "*" : 5 (2 * 3 - 1)
#  *******  # i = 4 , " " : 1 (5 - 4), "*" : 7 (2 * 4 - 1)
# ********* # i = 5 , " " : 0 (5 - 5), "*" : 9 (2 * 5 - 1)

i = 1
while i <= N :
    space = N - i
    star = 2 * i - 1
    print(" " * space + "*" * star)
    i += 1

#  *******  # i=1 ," " : 1, "*" : 7  (2 * (5 - 1) - 1)
#   *****   # i=2 ," " : 2, "*" : 5  (2 * (5 - 2) - 1)
#    ***    # i=3 ," " : 3, "*" : 3  (2 * (5 - 3) - 1)
#     *     # i=4 ," " : 4, "*" : 1  (2 * (5 - 4) - 1)

i = 1
while i < N:
    space = i
    star = 2 * (N - i) - 1
    print(" " * space + "*" * star)
    i += 1

