N = int(input())

#     *       # " " : 4 (1), "*" : 1 (1) 
#    ***      # " " : 3 (2), "*" : 3 (2) 
#   *****     # " " : 2 (3), "*" : 5 (3) 
#  *******    # " " : 1 (4), "*" : 7 (4) 
# *********   # " " : 0 (5), "*" : 9 (5) 
for i in range(1, N + 1):
    space = N - i
    star = 2 * i - 1
    print(" " * space + "*" * star)

# *******    # " " : 1, "*" : 7 (2 * (5 - 1)-1)
#  *****     # " " : 2, "*" : 5 (2 * (5 - 2)-1)
#   ***      # " " : 3, "*" : 3 (2 * (5 - 3)-1)
#    *       # " " : 4, "*" : 1 (2 * (5 - 4)-1)
for i in range(1, N):
    space = i
    star = 2 * (N - i) - 1
    print(" " * space + "*" * star)
